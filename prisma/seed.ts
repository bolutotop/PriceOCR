async function main() {
  console.log('开始导入基础数据...');

  for (const [categoryName, products] of Object.entries(dataMap)) {
    // 1. 创建或获取分类
    const category = await prisma.category.upsert({
      where: { name: categoryName },
      update: {},
      create: { 
        name: categoryName,
        // 这里可以给不同分类加一个默认排序权重，目前先默认为0
        order: 0 
      },
    });

    console.log(`正在处理分类: ${categoryName} (包含 ${products.length} 种烟)`);

    // 2. 创建或关联产品
    for (const productName of products) {
      await prisma.product.upsert({
        where: { name: productName },
        update: {
          categoryId: category.id, // 如果已经存在，更新其分类（防止之前归类错误）
        },
        create: {
          name: productName,
          alias: productName, // 默认别名等于原名
          categoryId: category.id,
        },
      });
    }
  }

  console.log('数据导入完成！');
}

main()
  .then(async () => {
    await prisma.$disconnect();
  })
  .catch(async (e) => {
    console.error(e);
    await prisma.$disconnect();
    process.exit(1);
  });